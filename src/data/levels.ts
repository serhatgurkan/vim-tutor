export interface VimLevel {
  id: number;
  keys: string[]; // Actual keys to detect (e.g., ['$'])
  displayKeys?: string[]; // Keys to show user (e.g., ['Shift', '4'])
  description: string;
  hint?: string;
  category: string;
  difficulty: "beginner" | "intermediate" | "advanced";
  // Mode info
  startMode?: "normal" | "insert" | "visual" | "visual-line" | "command";
  endMode?: "normal" | "insert" | "visual" | "visual-line" | "command";
  // Animation data
  beforeText: string;
  afterText: string;
  cursorBefore: number; // cursor position before command
  cursorAfter: number; // cursor position after command
  highlightBefore?: [number, number]; // selection range before
  highlightAfter?: [number, number]; // selection range after
  // Command line (for :commands)
  commandLine?: string;
}

